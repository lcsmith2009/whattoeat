function normalizeBudget(value){
  if (value === 'low') return 'cheap';
  if (value === 'medium') return 'mid';
  if (value === 'high') return 'treat';
  return value || 'mid';
}

function recentIds(limit=14){
  return state.history.slice(0, limit).map(item => item.id);
}

function scoreMealForSmartPick(meal, mood, budget, energy){
  const recent = recentIds(14);
  const veryRecent = recentIds(5);
  const hour = new Date().getHours();
  const moodList = Array.isArray(meal.mood) ? meal.mood : [meal.mood];
  let score = 8 + Math.random() * 9;

  if (moodList.includes(mood)) score += 42;
  if (mood === 'late' && moodList.includes('late')) score += 10;
  if (mood === 'lazy' && (moodList.includes('lazy') || meal.energy === 'low')) score += 16;
  if (mood === 'chaos' && (moodList.includes('chaos') || meal.category === 'snack')) score += 16;
  if (meal.budget === budget) score += 22;
  if (meal.energy === energy) score += 22;

  // Time of day nudges without destroying the user's actual choices.
  if (hour >= 22 || hour < 5) {
    if (moodList.includes('late')) score += 18;
    if (meal.energy === 'low') score += 8;
  } else if (hour >= 6 && hour < 11) {
    if (meal.category?.includes('bowl') || meal.category?.includes('toast')) score += 5;
  } else if (hour >= 17 && hour < 21) {
    if (moodList.includes('comfort') || meal.energy !== 'high') score += 8;
  }

  // Learn from saves and repeat behavior, but don't let the app become repetitive.
  if (state.saved.includes(meal.id)) score += 6;
  if (veryRecent.includes(meal.id)) score -= 90;
  else if (recent.includes(meal.id)) score -= 32;
  if (meal.id === state.lastSmartMealId) score -= 120;

  return score;
}

function weightedChoice(scored){
  const floor = Math.min(...scored.map(x => x.score));
  const weighted = scored.map(x => ({...x, weight: Math.max(1, x.score - floor + 1)}));
  const total = weighted.reduce((sum, x) => sum + x.weight, 0);
  let roll = Math.random() * total;
  for (const item of weighted) {
    roll -= item.weight;
    if (roll <= 0) return item.m;
  }
  return weighted[0]?.m || meals[0];
}

function smartPick(){
  const mood = state.picks.mood || state.defaultMood || 'comfort';
  const budget = normalizeBudget(state.picks.budget || 'mid');
  const energy = state.picks.energy || 'medium';
  const scored = meals
    .map(m => ({m, score: scoreMealForSmartPick(m, mood, budget, energy)}))
    .sort((a,b) => b.score - a.score);

  // Pick from the strongest matches, with weighted randomness so results feel fresh.
  let candidatePool = scored.slice(0, Math.min(24, scored.length));
  if (candidatePool.length > 1) {
    candidatePool = candidatePool.filter(item => item.m.id !== state.lastSmartMealId) || candidatePool;
  }
  let chosen = weightedChoice(candidatePool);

  // Final safety: never show the same exact meal twice in a row.
  if (state.lastSmartMealId && chosen.id === state.lastSmartMealId && scored.length > 1) {
    chosen = scored.find(item => item.m.id !== state.lastSmartMealId)?.m || chosen;
  }

  state.lastMeal = chosen;
  state.lastSmartMealId = chosen.id;
  state.rerollCount += 1;
  rememberPick(chosen, 'smart');
  saveState();

  const result = $('#smartResult');
  result.classList.remove('result-pop');
  result.innerHTML = `<div class="glass-card result-header"><p class="eyebrow">Smart pick #${state.rerollCount}</p><p class="tiny-note">Phase 1B.1 engine: smarter matching, less repeat nonsense, same fun UI.</p></div>${mealCard(chosen,true)}<button class="ghost-btn full" onclick="smartPick()">Reroll, I deserve options</button>`;
  requestAnimationFrame(()=>result.classList.add('result-pop'));
  toast(`${chosen.name} picked for your ${mood} mood`);
}

